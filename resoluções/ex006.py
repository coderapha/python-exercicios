numero = int(input('Digite um número: '))
dobro = numero * 2
triplo = numero * 3
raiz2 = numero ** (1/2)  # numero elevado a meio
print('O dobro de {} é {}'.format(numero, dobro))
print('O triplo de {} é {}'.format(numero, triplo))
print('A raiz quadrade de {} é {:.2f}'.format(numero, raiz2))
